package methodResult;

public class Calculator {
	double getAdd(double x, double y) {
		return x + y;
	}
	
	double getSub(double x, double y) {
		return x - y;
	}
	
	double getMul(double x, double y) {
		return x * y;
	}
	
	double getDiv(double x, double y) {
		return x / y;
	}
}
